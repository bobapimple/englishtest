// –§–ª–∞–≥ –¥–ª—è –æ—Ç—Å–ª–µ–∂–∏–≤–∞–Ω–∏—è —Å–æ—Å—Ç–æ—è–Ω–∏—è –æ—Ç–æ–±—Ä–∞–∂–µ–Ω–∏—è –æ—Ç–≤–µ—Ç–æ–≤
let answersVisible = false;

// –§—É–Ω–∫—Ü–∏—è –¥–ª—è –ø–µ—Ä–µ–∫–ª—é—á–µ–Ω–∏—è –≤–∏–¥–∏–º–æ—Å—Ç–∏ –æ—Ç–≤–µ—Ç–æ–≤
function toggleAnswers() {
    const answers = document.querySelectorAll('.answer');
    const showAnswersBtn = document.getElementById('show-answers-btn');
    
    answersVisible = !answersVisible;
    
    answers.forEach(answer => {
        if (answersVisible) {
            answer.classList.remove('hidden');
        } else {
            answer.classList.add('hidden');
        }
    });
    
    // –û–±–Ω–æ–≤–ª—è–µ–º —Ç–µ–∫—Å—Ç –∫–Ω–æ–ø–∫–∏
    showAnswersBtn.textContent = answersVisible ? 'üôà Hide Answers' : 'üëÅÔ∏è Show Answers';
}

// –î–æ–±–∞–≤–ª—è–µ–º –æ–±—Ä–∞–±–æ—Ç—á–∏–∫ —Å–æ–±—ã—Ç–∏—è –¥–ª—è –∫–Ω–æ–ø–∫–∏ "Show Answers"
document.addEventListener('DOMContentLoaded', function() {
    const showAnswersBtn = document.getElementById('show-answers-btn');
    const backBtn = document.getElementById('back-btn');
    
    if (showAnswersBtn) {
        showAnswersBtn.addEventListener('click', toggleAnswers);
    }
    
    if (backBtn) {
        backBtn.addEventListener('click', function() {
            // –ü–µ—Ä–µ–Ω–∞–ø—Ä–∞–≤–ª—è–µ–º –Ω–∞ –≥–ª–∞–≤–Ω—É—é —Å—Ç—Ä–∞–Ω–∏—Ü—É
            window.location.href = 'index.html';
        });
    }
});

// –§—É–Ω–∫—Ü–∏—è –¥–ª—è –ø–µ—Ä–µ–∫–ª—é—á–µ–Ω–∏—è —Ç–µ–º—ã (–µ—Å–ª–∏ –Ω—É–∂–Ω–æ)
function toggleTheme() {
    const currentTheme = document.body.getAttribute('data-theme');
    if (currentTheme === 'dark') {
        document.body.removeAttribute('data-theme');
        // –°–æ—Ö—Ä–∞–Ω—è–µ–º –≤—ã–±–æ—Ä —Ç–µ–º—ã –≤ localStorage (–µ—Å–ª–∏ –Ω—É–∂–Ω–æ)
        localStorage.setItem('theme', 'light');
    } else {
        document.body.setAttribute('data-theme', 'dark');
        // –°–æ—Ö—Ä–∞–Ω—è–µ–º –≤—ã–±–æ—Ä —Ç–µ–º—ã –≤ localStorage (–µ—Å–ª–∏ –Ω—É–∂–Ω–æ)
        localStorage.setItem('theme', 'dark');
    }
}

// –ó–∞–≥—Ä—É–∂–∞–µ–º —Å–æ—Ö—Ä–∞–Ω–µ–Ω–Ω—É—é —Ç–µ–º—É –ø—Ä–∏ –∑–∞–≥—Ä—É–∑–∫–µ —Å—Ç—Ä–∞–Ω–∏—Ü—ã (–µ—Å–ª–∏ –Ω—É–∂–Ω–æ)
document.addEventListener('DOMContentLoaded', function() {
    const savedTheme = localStorage.getItem('theme');
    if (savedTheme === 'dark') {
        document.body.setAttribute('data-theme', 'dark');
    }
});